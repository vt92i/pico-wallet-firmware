
import numpy as np
from PIL import Image

def extract_char_bitmap(char_bytes):
    """Convert 5 bytes (columns) to 5x8 pixel matrix."""
    bitmap = np.zeros((8, 5), dtype=np.uint8)
    for x in range(5):
        col = char_bytes[x]
        for y in range(8):
            if col & (1 << y):
                bitmap[y, x] = 1
    return bitmap

def resize_bitmap(bitmap, new_w, new_h):
    """Resample 2D bitmap to new size."""
    img = Image.fromarray(bitmap * 255)  # 0 or 255
    img = img.resize((new_w, new_h), resample=Image.NEAREST)
    resized = np.array(img) > 127
    return resized.astype(np.uint8)

def bitmap_to_column_bytes(bitmap):
    """Convert resized bitmap to column-wise byte format."""
    h, w = bitmap.shape
    columns = []
    bytes_per_col = (h + 7) // 8
    for x in range(w):
        for b in range(bytes_per_col):
            byte = 0
            for bit in range(8):
                y = b * 8 + bit
                if y < h and bitmap[y, x]:
                    byte |= (1 << bit)
            columns.append(byte)
    return columns, bytes_per_col

def scale_font_custom(font_data, new_w, new_h):
    height, width, _, first_char, last_char = font_data[:5]
    font_bytes = font_data[5:]
    num_chars = last_char - first_char + 1
    new_font = [new_h, new_w, (new_h + 7) // 8, first_char, last_char]

    for i in range(num_chars):
        char = font_bytes[i * width:(i + 1) * width]
        original = extract_char_bitmap(char)
        resized = resize_bitmap(original, new_w, new_h)
        col_bytes, _ = bitmap_to_column_bytes(resized)
        new_font.extend(col_bytes)

    return new_font

